/**
 * Пример
 * Используя цикл for
 * выведите 5 10 15 20 25
 */
function task0()
{
    // начинаем с 5 $i = 5;
    // последнее значение 25 $i <= 25;
    // вычмсляем шаг 10 - 5 = 15 -10 = 5 $i+=5;
    // для вывода строки используем echo
    // для переноса строки PHP_EOL

    for ($i = 5; $i <= 25; $i += 5) {
        echo $i, PHP_EOL;
    }
}

